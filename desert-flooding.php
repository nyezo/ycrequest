								<h3>Estimated Costs</h3>
								<p>Three key areas of capital cost - desalination plants, installed electricity generation capacity and the piping system - put the cost of the project well above $50tn already. Economies of scale will thus both be necessary as well as breakthrough in material science and construction technology.</p>
								<p>The desalination plants need an annual 9tn m3 capacity, which translates to a rate 24.7tn litres of water per day. Current plants in Saudi-Arabia have a capital cost of $10 per l per day capacity, but we can expect $1 per liter per day going forward based on low cost plants. This would result in a roughly $25tn capital cost for building desalinating capacity.</p>
								<p>For installed electricity generation capacity, the likely option assuming current technologies would be large-scale solar, which we could assume at that scale to fall under $1 per watt of installed cost. The effectiveness of desalination is assumed to be 3.5wh per liter, which leads us to 31.5tn kwh needed per year or a 3.6TW installed capacity. This gives us an installed cost of $3.6 trillion dollars to supply the desalination plants with water.</p>
								<p>The cost of installing the piping system with current technologies is also significant, even when stretching them. Assuming each 2 reservoirs can be supplied with 1km of additional piping, 2.25mn km of piping are needed. At a 12f/s flow velocity, which results in average pressures, a 8" pipe can support about 430k l / hour, which means it can supply 2 reservoirs constantly. At a $120k/in-km installed cost for onshore pipeline, the total cost equals $25.9 tn.</p>
