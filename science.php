						<p>At its core, the issue of ocean acidification is simple chemistry. There are two important things to remember about what happens when carbon dioxide dissolves in seawater. First, the pH of seawater water gets lower as it becomes more acidic. Second, this process binds up carbonate ions and makes them less abundant—ions that corals, oysters, mussels, and many other shelled organisms need to build shells and skeletons.</p>
						<p>When water (H2O) and CO2 mix, they combine to form carbonic acid (H2CO3). Carbonic acid is weak compared to some of the well-known acids that break down solids, such as hydrochloric acid (the main ingredient in gastric acid, which digests food in your stomach) and sulfuric acid (the main ingredient in car batteries, which can burn your skin with just a drop). The weaker carbonic acid may not act as quickly, but it works the same way as all acids: it releases hydrogen ions (H+), which bond with other molecules in the area</p>
