						<h2>Ocean acidification & alkalinity</h2>
						<p>When CO2 dissolves in seawater, the water becomes more acidic and the ocean’s pH (a measure of how acidic or basic the ocean is) drops. At first, scientists thought that this might be a good thing because it leaves less CO2 in the air to warm the planet. But in the past decade, scientists realized that this slowed warming has come at the cost of changing the ocean’s chemistry. Even though the ocean is immense, enough CO2 can have a major impact. In the past 200 years alone, ocean water has become 30 percent more acidic—faster than any known change in ocean chemistry in the last 50 million years.</p>
						<p>Alkalinity refers to the capability of water to neutralize acid. If the ocean's alkalinity can be increased it can absorb more CO2.</p>
					</div>
				</div>
				<div class="group text-item">
